    private void btnCompostoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompostoActionPerformed
        // TODO add your handling code here:
        double capital = Double.parseDouble(txtAnarcoCap.getText());
        double tempo = Double.parseDouble(txtYears.getText());
        double index = Double.parseDouble(txtIndex.getText())/100;
        
        String compostal = juroCompostal(capital, tempo, index);
        DecimalFormat warlino = new DecimalFormat("#.####");
        DecimalFormat warleyArrumaNamoradaPeloAmorDeDeus = new DecimalFormat("#.##");
        
        lblAnarcoCap.setText("<html>" + warleyArrumaNamoradaPeloAmorDeDeus.format(capital) + " * ( 1 + "
                                      + warlino.format(index) +")<sup>" + warlino.format(tempo) 
                                      + "</sup></html>");
        lblMontante.setText("R$ " + compostal);
        
    }//GEN-LAST:event_btnCompostoActionPerformed

