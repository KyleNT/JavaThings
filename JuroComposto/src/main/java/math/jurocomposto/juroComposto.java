    public String juroCompostal(double cap, double tmp, double ind){
        double montante;
        double indexOne;
        double timerset;
        DecimalFormat warleyStrikesBack = new DecimalFormat("#.##");
        
        
        // C * (1+i)^t
        
        indexOne = 1 + ind;
        timerset = Math.pow(indexOne, tmp);
        montante = cap * timerset;
        
        return warleyStrikesBack.format(montante);
        
    }
